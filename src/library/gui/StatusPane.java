    private void checkCopyStatusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkCopyStatusButtonActionPerformed
         try {
             String callNumber = callNumberField.getText();
             int copyNumber = copyNumberField.getText().length() == 0
                        ? 1
                        : Integer.parseInt(copyNumberField.getText());                      
             Copy copy = LibraryDatabase.getInstance().getCopy(
                        callNumber, copyNumber);
             StatusUseCase.perform(copy);
         }
        catch(NumberFormatException e) {
            GUI.getInstance().showMessage("Copy number must be an integer");
        }
        catch(IllegalArgumentException e) {
            GUI.getInstance().showMessage(e.getMessage());
        }
        callNumberField.setText("");
        copyNumberField.setText("");
        callNumberField.requestFocus();
        checkCopyStatusButton.setEnabled(false);
    }//GEN-LAST:event_checkCopyStatusButtonActionPerformed

