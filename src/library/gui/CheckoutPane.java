    /** Rent items when the OK button is clicked */
    private void checkoutCopiesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkoutCopiesButtonActionPerformed
        String phoneNumber = checkoutToPatronField.getText();
        try {
            Patron patron = LibraryDatabase.getInstance().getPatron(phoneNumber);
            CheckoutUseCase useCase = new CheckoutUseCase(patron);
            CheckoutDetailsCard.getInstance().setUseCase(useCase);
            GUI.getInstance().gotoCard("checkoutdetails");
         }
        catch(IllegalArgumentException e) {
            GUI.getInstance().showMessage(e.getMessage());
        }
        checkoutToPatronField.setText("");
        checkoutToPatronField.requestFocus();
    }//GEN-LAST:event_checkoutCopiesButtonActionPerformed
    
    
